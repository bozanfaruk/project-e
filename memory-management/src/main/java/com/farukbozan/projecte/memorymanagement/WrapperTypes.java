package com.farukbozan.projecte.memorymanagement;

public class WrapperTypes {

    private final Integer intValue = 1;

    private final Long longValue = 2L;

    private final Float floatValue = 3.0f;

    private final Double doubleValue = 4.0d;

    private final Short shortValue = 5;

    private final Byte byteValue = 6;

    private final Character characterValue = '7';

    private final Boolean booleanValue = Boolean.TRUE;

    public Integer getIntValue() {
        return intValue;
    }

    public Long getLongValue() {
        return longValue;
    }

    public Float getFloatValue() {
        return floatValue;
    }

    public Double getDoubleValue() {
        return doubleValue;
    }

    public Short getShortValue() {
        return shortValue;
    }

    public Byte getByteValue() {
        return byteValue;
    }

    public Character getCharacterValue() {
        return characterValue;
    }

    public Boolean getBooleanValue() {
        return booleanValue;
    }
}
